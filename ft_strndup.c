#include "libft.h"

char	*ft_strndup(const char *src, int n)
{
	char	*copy;
	int		i;

	i = 0;
	if (!(copy = (char *)malloc(n + 1)))
		return (0);
	i = 0;
	while (src[i] && i < n)
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
